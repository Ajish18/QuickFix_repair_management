"""
MONKEY PATCHES FOR QUICKFIX APP
=================================

Each patch is documented with:
- WHY it was needed
- WHAT it changes
- FRAPPE VERSION it was tested on
- RISK if frappe updates the patched function
- TEST that validates the patch still works
"""

import frappe


def apply_all():
    """Apply all monkey patches"""
    _patch_get_url()


def _patch_get_url():
    """
    WHY:
    The site may use a custom CDN prefix stored in site_config.json

    WHAT:
    Prepends custom_url_prefix to all URLs generated by frappe.utils.get_url()

    VERSION TESTED:
    frappe v16

    RISK:
    If frappe changes get_url() parameters this patch may break.

    TEST:
    tests/test_monkey_patches.py
    """

    import frappe.utils as fu

    # Guard → do not apply patch twice
    if hasattr(fu, "_qf_patched"):
        return

    # Save original function
    _orig = fu.get_url

    # Custom replacement function
    def _custom_get_url(path=None, full_address=False):

        url = _orig(path, full_address)

        prefix = frappe.conf.get("custom_url_prefix", "")

        if prefix:
            return prefix + url

        return url

    # Replace original function
    fu.get_url = _custom_get_url

    # Mark patched
    fu._qf_patched = True